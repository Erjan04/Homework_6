package com.company;

public class Player {

    private int characterHealth;
    private int characterDamage;
    private int characterDefence;
    private int characterSuperPower;

    public int getCharacterHealth() {
        return characterHealth;
    }

    public void setCharacterHealth(int characterHealth) {
        this.characterHealth = characterHealth;
    }

    public int getCharacterDamage() {
        return characterDamage;
    }

    public void setCharacterDamage(int characterDamage) {
        this.characterDamage = characterDamage;
    }

    public int getCharacterDefence() {
        return characterDefence;
    }

    public void setCharacterDefence(int characterDefence) {
        this.characterDefence = characterDefence;
    }

    public int getCharacterSuperPower() {
        return characterSuperPower;
    }

    public void setCharacterSuperPower(int characterSuperPower) {
        this.characterSuperPower = characterSuperPower;
    }
}
